package dataTypes;

public class DataTypes {
    public static void main(String[] args){
        int num = 10;
        float rateOfInterest = 3.77f;
        double pie = 3.142;
        boolean isThisReallyHelpful = true;
        char at = '@';

        System.out.println("printing all types of Datatypes value \n");
        System.out.println("integer num = " + num);
        System.out.println("float rateOfInterest = " + rateOfInterest);
        System.out.println("double pie = "+ pie);
        System.out.println("boolean isThisReallyHelpful = " + isThisReallyHelpful);
        System.out.println("char at = "+ at);
    }
}
